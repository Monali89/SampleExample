package javaarray;

public class Multiple_if {

	public static void main(String[] args) 
	{
		 int age=25;
		 if(age<18)
		 {
			 System.out.println("you cant travel");
		 }
		 if(age>18)
		 {
			 System.out.println("you can travel");
		 }
		 if(age<=18)
		 {
			 System.out.println("you can travel but pay fare");
		 }
		 if(age>=18)
		 {
			 System.out.println("you pay full fare");
		 }
		 if(age!=18)
		 {
			 System.out.println("you are not eligible");
		 }
		 if(age==18)
		 {
			 System.out.println("you can do anything");
		 }
		 
		 

	}

}
